#Compound Assignment
#การแปลงค่า
x = 10
print('+-------------------+')
print('Befor : '+str(x))
x = x+5
print('After : '+str(x))
print('+-------------------+')
#การเขียนแบบ Compound Assignment
x += 1
print('Compound Assignment : '+str(x))
'''
Compound Assignment
+= บวก
-= ลบ
*= คูณ
/= หาร
//= หารตัดเศษ
**= ยกกำลัง
%= หารเอาเศษ
'''